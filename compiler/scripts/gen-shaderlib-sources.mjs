// gen-shaderlib-sources.mjs — embeds compiler/shaderlib/*.ts into src/shaderlib-sources.ts
// so the BROWSER bundle (lz-ts) can load the shaderlib without fs (esbuild --platform=browser
// turns fs requires into throw-shims). Run via `npm run gen:shaderlib` after editing ports.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const LIB = path.join(HERE, "../shaderlib");
const OUT = path.join(HERE, "../src/shaderlib-sources.ts");

const namespaces = ["noise", "shape", "pal", "color", "math"];
const entries = namespaces.map((ns) => {
  const src = fs.readFileSync(path.join(LIB, ns + ".ts"), "utf8");
  return `  ${JSON.stringify(ns)}: ${JSON.stringify(src)},`;
});

fs.writeFileSync(OUT, `// GENERATED by scripts/gen-shaderlib-sources.mjs — do not edit.
// The curated shaderlib port sources, embedded so the browser bundle needs no fs.
export const SHADERLIB_SOURCES: Record<string, string> = {
${entries.join("\n")}
};
`);
console.log("wrote", OUT);
